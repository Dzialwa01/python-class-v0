# Comparison predictions

print(True == False) # False
print(False == False) # True
print(False != True) # True
print((not True) == False) # True

print((2 + 3) == 5) # True
print(4 < 0) # False
print(10 >= 10) # True
print(10.3 >= 10) # True

print((100 / 2) == 50) # True
print((100 % 2) == 0) # True 
print((11 % 2) == 0) # False
print(7.0 == 7) # True

print((13 % 5) > 0) # True
print("potato" == "potato") # True
print("Tomato" == "tomato") # False (Python is case sensitive)
print("42" == 42) # False (String cannot equal integer)
print(5 > 3 and 1 == 0) # False
