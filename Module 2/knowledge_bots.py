# 1. What is a knowledge Bot
# A knowledge bot is a smart helper that answers questions using information you give it. (Using company documents)
#E.g School chatbot, Bank WhatsApp chatbot, Hospital/clinic information, company helpdesk bot
# Simple analogy - A knowledge bot is like a student studying notes. it can only answer from what it has learned.

# 2. Why do businesses use knowledge bots?
# To answer questions faster and also many people at the same time
# Help customers 24/7
# Help new employees learn
# Reduce training time 
# Save money and time

## Example tasks:
# Task - How a bot helps 
# Ask school fees - Bot tells fee info
# Ask HR about leave - Bot gives policy
# Ask shop about product - Bot give details

# 3. Business Knowledge (Simple IP)
# There are two types of information: 
# Type - Meaning - Example
# Public info - Anyone can see - website,brochurs
# Private info / company secrets - Only for business - staff rules, price,lists
# Rule for students - Never put private company info into public AI tools

# 4. When AI Can Be Wrong (Hallucinations)
# Sometimes AI sounds right but is wrong, this is called Hallucinations
# To avoid this : use real company documents, update information and check answers before trusting them.

